SELECT A.MEMBER_NAME, C.REVIEW_TEXT, TO_CHAR(C.REVIEW_DATE,'YYYY-MM-DD') AS REVIEW_DATE
FROM MEMBER_PROFILE A, 
(SELECT MEMBER_ID FROM REST_REVIEW GROUP BY MEMBER_ID ORDER BY COUNT(MEMBER_ID) DESC FETCH NEXT 1 ROW ONLY) B,
(SELECT MEMBER_ID, REVIEW_TEXT, REVIEW_DATE FROM REST_REVIEW) C
WHERE A.MEMBER_ID = B.MEMBER_ID AND B.MEMBER_ID = C.MEMBER_ID
ORDER BY C.REVIEW_DATE, C.REVIEW_TEXT